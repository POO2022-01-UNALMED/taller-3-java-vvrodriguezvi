package taller3.televisores;

public class Marca {
	private String nombre;
	
	//Constructor
	
	public Marca(String nombre) {
		this.nombre = nombre;
	}
	
	//Getter and setter
	
	public void setNombre(String nombre) {
			this.nombre = nombre;	
	}
	
	public String getNombre() {
		return nombre;		
	}
}
